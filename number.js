//數字及其用法

document.write((6+5)*3);
document.write("<br>");

var number = -25;
document.write(number);
document.write("<br>");

//絕對值
document.write(Math.abs(number));
document.write("<br>");

//最大和最小值
document.write(Math.max(10, 20, 55, 16, 100)); //100
document.write("<br>");
document.write(Math.min(5, 8, 100, 59, 21)); //5
document.write("<br>");

//四捨五入
document.write(Math.round(2.6)); //3
document.write("<br>");

//次方
document.write(Math.pow(5,3)); //125
document.write("<br>");

//根號
document.write(Math.sqrt(121)); //11
document.write("<br>");

//隨機數
document.write(Math.round(Math.random()*10)); //0~10的隨機數
document.write("<br>");